package com.training.exception;

public class AccountPartyException extends Exception {

	public AccountPartyException() {
		super();

	}

	public AccountPartyException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public AccountPartyException(String arg0) {
		super(arg0);
	}

	public AccountPartyException(Throwable arg0) {
		super(arg0);
	}

}
